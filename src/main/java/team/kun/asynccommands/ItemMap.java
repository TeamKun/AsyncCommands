    @Nullable
    public static Material findMaterialById(String id) {
        ItemMap result = Arrays.stream(ItemMap.values()).filter(itemMap -> itemMap.id.equals(id)).findFirst().orElse(null);
        if (result == null) {
            return null;
        }
        return result.material;
    }
